package OOP.polymorphism;

public class Summation {
	public int addition(int a, int b) {
		int total = a + b;
		return total;
	}		
	public int addition(int a, int b, int c) {
		int total = a + b +c;
		return total;
	}
	public int addition(int a, int b, int c, int d) {
		int total = a + b + c + d;
		return total;
	}
}